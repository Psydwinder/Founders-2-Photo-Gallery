const readMoreBtn1 = document.querySelector(".btn1");
const desc = document.querySelector(".description1");

readMoreBtn1.addEventListener("click", (e) => {
  desc.classList.toggle("show-more");
  if (readMoreBtn1.innerText === "+") {
    readMoreBtn1.innerText = "-";
  } else {
    readMoreBtn1.innerText = "+";
  }
});

